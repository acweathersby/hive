// Quick primitive type reference for GLSL types.
unsigned hive::gl::getGLSLTypePrimitive(GLint t)
{
    switch (t) {
    case GL_FLOAT:
        return 2;
    case GL_FLOAT_VEC2:
        return 2;
    case GL_FLOAT_VEC3:
        return 2;
    case GL_FLOAT_VEC4:
        return 2;
    case GL_FLOAT_MAT2:
        return 2;
    case GL_FLOAT_MAT3:
        return 2;
    case GL_FLOAT_MAT4:
        return 2;
    case GL_FLOAT_MAT2x3:
        return 2;
    case GL_FLOAT_MAT3x2:
        return 2;
    case GL_FLOAT_MAT2x4:
        return 2;
    case GL_FLOAT_MAT4x2:
        return 2;
    case GL_FLOAT_MAT3x4:
        return 2;
    case GL_FLOAT_MAT4x3:
        return 2;
    case GL_INT:
        return 1;
    case GL_INT_VEC2:
        return 1;
    case GL_INT_VEC3:
        return 1;
    case GL_INT_VEC4:
        return 1;
    case GL_UNSIGNED_INT:
        return 0;
    case GL_UNSIGNED_INT_VEC2:
        return 0;
    case GL_UNSIGNED_INT_VEC3:
        return 0;
    case GL_UNSIGNED_INT_VEC4:
        return 0;
    case GL_DOUBLE:
        return 3;
    case GL_DOUBLE_VEC2:
        return 3;
    case GL_DOUBLE_VEC3:
        return 3;
    case GL_DOUBLE_VEC4:
        return 3;
    case GL_DOUBLE_MAT2:
        return 3;
    case GL_DOUBLE_MAT3:
        return 3;
    case GL_DOUBLE_MAT4:
        return 3;
    case GL_DOUBLE_MAT2x3:
        return 3;
    case GL_DOUBLE_MAT3x2:
        return 3;
    case GL_DOUBLE_MAT2x4:
        return 3;
    case GL_DOUBLE_MAT4x2:
        return 3;
    case GL_DOUBLE_MAT3x4:
        return 3;
    case GL_DOUBLE_MAT4x3:
        return 3;
    }
    return 0;
};

